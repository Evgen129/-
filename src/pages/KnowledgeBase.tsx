function KnowledgeTable({ table }: { table: TableData }) {
  return (
    <div className="overflow-x-auto rounded-lg border border-slate-700">
      <table className="min-w-full divide-y divide-slate-700 text-left text-sm">
        <thead className="bg-slate-900/80 text-xs uppercase tracking-wide text-slate-400">
          <tr>
            {table.headers.map(header => (
              <th key={header} className="px-3 py-2 font-semibold">
                {header}
              </th>
            ))}
          </tr>
        </thead>
        <tbody className="divide-y divide-slate-700">
          {table.rows.map((row, rowIndex) => (
            <tr key={`${row[0]}-${rowIndex}`} className="bg-slate-800/60">
              {row.map((cell, cellIndex) => (
                <td key={`${cell}-${cellIndex}`} className="px-3 py-2 text-slate-300">
                  {cell}
                </td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

function KnowledgeCard({ block }: { block: KnowledgeBlock; key?: string }) {
  return (
    <section className="rounded-xl border border-slate-700 bg-slate-800 p-4 shadow-sm">
      <h2 className="mb-3 text-lg font-semibold text-slate-100">{block.title}</h2>
      {block.paragraphs?.map(paragraph => (
        <p key={paragraph} className="mb-3 text-sm leading-6 text-slate-400">
          {paragraph}
        </p>
      ))}
      {block.items && (
        <ul className="space-y-2 text-sm leading-6 text-slate-300">
          {block.items.map(item => (
            <li key={item} className="flex gap-2">
              <span className="mt-2 h-1.5 w-1.5 shrink-0 rounded-full bg-blue-400" />
              <span>{item}</span>
            </li>
          ))}
        </ul>
      )}
      {block.table && <KnowledgeTable table={block.table} />}
    </section>
  );
}

